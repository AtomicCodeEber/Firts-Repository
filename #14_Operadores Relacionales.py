# Comparación en Base a Operadores Relacionales

print("\n         Comparación de Números \n")

firts=input("Introduce the 1st Number: ")
second=input("Introduce the 2nd Number: ")

print("\n         The Numbers Are",firts,"and",second,".", "\n")

if firts==second:
    print("Are the Same...")
if firts!=second:
    print("Are Diferent...")
if firts>second:
    print("Is Higher...")
if firts<second:
    print("Is Lower...")
if firts>=second:
    print("Are Higher or Equal...")
if firts<=second:
    print("Are Lower or Equal...")