# Existen distintos operadores capaces de realizar operaciones

# Variables
number1=5
number2=4
number3=1.5
exponent=2

# Sum (+)
sum=number1+number2
print("The Result Of The Sum Is:", str(sum))

# Rest (-)
rest=number1-number2
print("The Reulst Of The Rest Is:", str(rest))

# Multiplication (*)
multiplication=number1*number2
print("The Result Of The Multiplication Is:", str(multiplication))

# Potence (**)
potence=number2**exponent
print("The Result Of The Potence Is:", str(potence))

# Division (/)
division=number1/number2
print("The Resulto Of The Division Is:", str(division))

# Absolute Division (//)
absolute_division=number2//number3
print("The Result Of The Absolute Division Is:", str(absolute_division))

# Module
module=number1%number2
print("The Module Is Equal To:", str(module))