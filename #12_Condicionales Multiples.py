# Ejercicio de Conversión de Números a Letras

print("     //Programa Capaz de Convertir Dígitos en Texto//")
translate=int(input("Ingrese el Número que desea Textualizar: "))

if translate==1:
    print("Uno")
elif translate==2:
    print("Dos")
elif translate==3:
    print("Tres")
elif translate==4:
    print("Cuatro")
elif translate==5:
    print("Cinco")
else:
    print("El Programa Unicamente Admite como Máximo el Número 5")